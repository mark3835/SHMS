	ROC_ID VARCHAR(14) NOT NULL,
	ACCOUNT VARCHAR(40) NOT NULL,
	NAME VARCHAR(60) NOT NULL,
	UNIT_ID VARCHAR(40) NOT NULL,
	JOB_NAME VARCHAR(40) NOT NULL,
	JOB_LEVEL INT NOT NULL,
	PHONE VARCHAR(50),
	EMAIL VARCHAR(80),
	BIRTHDAY VARCHAR(40),
